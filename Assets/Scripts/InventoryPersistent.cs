using UnityEngine;

public class InventoryPersistent : MonoBehaviour
{
    public static InventoryPersistent instance;

    private void Awake()
    {
        if (instance != null && instance != this)
        {
            Destroy(gameObject);
        }
        else
        {
            instance = this;
            DontDestroyOnLoad(gameObject);
        }
    }
}
